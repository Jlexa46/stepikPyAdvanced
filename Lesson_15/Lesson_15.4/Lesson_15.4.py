# f(x)=a*x^2+b*x+c, –Ω–æ –ø–æ–≤–µ–¥–µ–Ω–∏–µ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–≥–æ –∫–≤–∞–¥—Ä–∞—Ç–Ω–æ–≥–æ —Ç—Ä–µ—Ö—á–ª–µ–Ω–∞ –∑–∞–≤–∏—Å–∏—Ç –æ—Ç –∑–Ω–∞—á–µ–Ω–∏—è –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤ a,b,c. –ú—ã –º–æ–∂–µ–º
# –Ω–∞–ø–∏—Å–∞—Ç—å –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä —Ñ—É–Ω–∫—Ü–∏–π, –∫–æ—Ç–æ—Ä—ã–π –ø–æ –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º a,b,c, –ø–æ—Å—Ç—Ä–æ–∏—Ç –∏ –≤–µ—Ä–Ω–µ—Ç –Ω–∞–º –∫–æ–Ω–∫—Ä–µ—Ç–Ω—ã–π –∫–≤–∞–¥—Ä–∞—Ç–Ω—ã–π —Ç—Ä–µ—Ö—á–ª–µ–Ω:
def generator_square_polynom(a, b, c):
    def square_polynom(x):
        return a*x**2 + b*x + c
    return square_polynom
f = generator_square_polynom(a=1, b=2, c=1)
g = generator_square_polynom(a=2, b=0, c=-3)
h = generator_square_polynom(a=-3, b=-10, c=50)
print(f(1))     # 4
print(g(2))     # 5
print(h(-1))    # 57

